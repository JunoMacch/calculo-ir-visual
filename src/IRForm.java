    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor insira um salário válido para realizar o cálculo", "Erro", 1);
                jTextField1.setText("");
                jTextField1.grabFocus();
                return;
            }

            Calculadora calculadora = new Calculadora();
            double salario = Double.parseDouble(jTextField1.getText());
            
            if (salario <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor insira um salário maior que zero.", "Erro", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField1.grabFocus();
            return;
            }
            
            double imposto = 0;
            double aliquotaEfetiva = 0;
            double salarioFinal = 0;

            if (jRadioButton1.isSelected() && salario > 0) {
                imposto = calculadora.calculoMensal(salario);
                jLabel7.setText("Total de Desconto: " + String.format("%.2f", imposto));

                aliquotaEfetiva = ((imposto / salario) * 100);
                jLabel5.setText("Alíquota Efetiva: " + String.format("%.2f", aliquotaEfetiva) + "%");

                salarioFinal = (salario - imposto);
                jLabel8.setText("Salario Final: " + String.format("%.2f", salarioFinal));
            }
            
            if (jRadioButton2.isSelected() && salario > 0) {
                imposto = calculadora.calculoAnual(salario);
                jLabel7.setText("Total de Desconto: " + String.format("%.2f", imposto));

                aliquotaEfetiva = ((imposto / salario) * 100);
                jLabel5.setText("Alíquota Efetiva: " + String.format("%.2f", aliquotaEfetiva) + "%");

                salarioFinal = (salario - imposto);
                jLabel8.setText("Salario Final: " + String.format("%.2f", salarioFinal));
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, insira um valor numérico válido para o salário.", "Erro", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField1.grabFocus();
        } catch (Exception ex) {
            if (jRadioButton1.isSelected() != true && jRadioButton2.isSelected() != true) {
                JOptionPane.showMessageDialog(null, "Por favor selecione uma opção para realizar o cálculo", "Erro", 1);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

